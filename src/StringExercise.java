public class StringExercise {
    public static void main(String[] args) {
        String pinkFloyd = "We don't need no ";
        System.out.println(pinkFloyd + "education");
        System.out.println(pinkFloyd + "thought control");

        String checkThisOutString = "Check \"this\" out! \"s inside of \"s! ";
        System.out.println(checkThisOutString);

        String windows = "In windows, the main drive is usually C: \\";
        System.out.println(windows);

        String iCando = "I can do backslashes \\, double backslashes \\\\,\n" +
                "and the amazing triple backslash \\\\\\!";
        System.out.println(iCando);
    }
}
